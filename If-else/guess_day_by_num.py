day_number = int(input("Enter the day number"))

if day_number == 1 :
	print("Monday")
elif day_number == 2 :
	print("Tuesday")
elif day_number == 3 :
	print("Wedneday")
elif day_number == 4 :
	print("Thursday")
elif day_number == 5 :
	print("Friday")
elif day_number == 6 :
	print("Saturday")
elif day_number == 7 :
	print("Sunday")
else :
	print("Please enter between 1 to 7")